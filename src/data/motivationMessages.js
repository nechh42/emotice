// Yardımcı fonksiyonlar
export const getMessagesByCategory = (category, language = 'tr') => {
  return motivationMessages.categories[category]?.[language] || [];
};

export const getAllCategories = () => {
  return Object.keys(motivationMessages.categories);
};

export const getRandomMessage = (category = 'motivation', language = 'tr') => {
  const messages = getMessagesByCategory(category, language);
  if (messages.length === 0) return null;
  
  const randomIndex = Math.floor(Math.random() * messages.length);
  return messages[randomIndex];
};

export const getMessageByLength = (category, language, length) => {
  const messages = getMessagesByCategory(category, language);
  const lengthRanges = motivationMessages.metadata.lengths;
  
  return messages.filter(message => {
    const msgLength = message.length;
    const [min, max] = lengthRanges[length].split('-').map(Number);
    return msgLength >= min && msgLength <= max;
  });
};

export const getTotalMessageCount = () => {
  let total = 0;
  const categories = motivationMessages.categories;
  
  Object.keys(categories).forEach(category => {
    Object.keys(categories[category]).forEach(language => {
      total += categories[category][language].length;
    });
  });
  
  return total;
};

